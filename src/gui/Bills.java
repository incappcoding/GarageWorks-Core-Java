    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // remove row from DB
        try{
            int ri=jTable2.getSelectedRow();
            if(ri!=-1){
                int id=(int)jTable2.getValueAt(ri,0);
                String name=(String)jTable2.getValueAt(ri,1);
                db.DbConnect.st.executeUpdate("delete from bill_items where bid="+id);
                db.DbConnect.st.executeUpdate("update spare_parts set qty=qty+1 where name='"+name+"'");
                getEntriesBillItems();
                JOptionPane.showMessageDialog(null, "Item Deleted Successfully!");
            }else{
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // save bill
        try{
            int ri=jTable1.getSelectedRow();
            if(ri!=-1){
                int id=(int)jTable1.getValueAt(ri,0);
                String name=(String)jTable1.getValueAt(ri,1);
                String phone=(String)jTable1.getValueAt(ri,2);
                String v_no=(String)jTable1.getValueAt(ri,3);
                String v_name=(String)jTable1.getValueAt(ri,4);
                int amt=Integer.parseInt(t.getText());
                db.DbConnect.st.executeUpdate("insert into bills (client_id,name,phone,amount,bill_date,v_no,v_name) values("+id+",'"+name+"','"+phone+"',"+amt+",CURRENT_DATE,'"+v_no+"','"+v_name+"')");
                db.DbConnect.st.executeUpdate("update clients set status='done' where id="+id);
                getEntries();
                getEntriesBillItems();
                t.setText("0");
                JOptionPane.showMessageDialog(null, "Bill saved Successfully!");
            }else{
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

