    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // add spare parts
        try{   
            String name=n.getText();
            String pr=p.getText();
            String qt=q.getText();
            
            if(name.equals("") || pr.equals("") || qt.equals("")){
                JOptionPane.showMessageDialog(null, "Please Fill the Complete Form!");
            }else{
                int price=Integer.parseInt(pr);
                int qty=Integer.parseInt(qt);
                db.DbConnect.st.executeUpdate("insert into spare_parts (name,price,qty) values('"+name+"',"+price+","+qty+")");
                getEntries();
                JOptionPane.showMessageDialog(null, "Spare Part Added Successfully!");
                n.setText(null);
                p.setText(null);
                q.setText(null);
            }
        }catch(java.sql.SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null, "Name already Exist!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

