    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Update Spare Part
        try{   
            int ri=jTable1.getSelectedRow();
            if(ri!=-1){
                String nm=(String)jTable1.getValueAt(ri,0);
                String name=n1.getText();
                String pr=p1.getText();
                String qt=q1.getText();

                if(name.equals("") || pr.equals("") || qt.equals("")){
                    JOptionPane.showMessageDialog(null, "Please Fill the Complete Form!");
                }else{
                    int price=Integer.parseInt(pr);
                    int qty=Integer.parseInt(qt);
                    db.DbConnect.st.executeUpdate("update spare_parts set name='"+name+"',price="+price+" ,qty="+qty+" where name='"+nm+"'");
                    getEntries();
                    JOptionPane.showMessageDialog(null, "Spare Part Updated Successfully!");
                    n1.setText(null);
                    p1.setText(null);
                    q1.setText(null);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        }catch(java.sql.SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null, "Name already Exist!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // remove row from DB
        try{
            int ri=jTable1.getSelectedRow();
            if(ri!=-1){
                String name=(String)jTable1.getValueAt(ri,0);
                db.DbConnect.st.executeUpdate("delete from spare_parts where name='"+name+"'");
                getEntries();
                JOptionPane.showMessageDialog(null, "Spare Part Deleted Successfully!");
            }else{
                JOptionPane.showMessageDialog(null, "Please Select any row!");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

